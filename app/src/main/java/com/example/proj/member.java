package com.example.proj;

public class member {
private String f_name,email;
private Long number;

    public member() {
        this.f_name = f_name;
        this.email = email;
        this.number = number;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public Long getNumber() {
        return number;
    }

    public void setNumber(Long number) {
        this.number = number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }


}
